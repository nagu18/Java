public class Sample{
    public static void main(String[] args){
        int x=0;
        while(x<10){
            x++;
            System.out.println(x);

        }
    }
}

//simple loop that prints numbers from 1 to 10. 
// The while loop continues to execute as long as the condition (x < 10) is true. 
// Inside the loop, we increment x by 1 and print its value.